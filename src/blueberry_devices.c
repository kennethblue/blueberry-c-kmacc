	setBbFloat32(buf, msg, OSCOPE_CONFIG_MESSAGE_HOLDOFF_INDEX, holdoff);
	setBbUint8(buf, msg, OSCOPE_CONFIG_MESSAGE_TRIG_POS_INDEX, trigPos);
	setBbBool(buf, msg, OSCOPE_CONFIG_MESSAGE_RISING_NOT_FALLING_INDEX, OSCOPE_CONFIG_MESSAGE_RISING_NOT_FALLING_MASK, risingNotFalling);
