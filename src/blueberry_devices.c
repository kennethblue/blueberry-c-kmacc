/**
 * Adds a Spi Transaction Message to the end of the current buffer
 *  A message to define an SPI transaction to be sent out
 * @param buf - the message buffer to add the message to
 * @param spiDev
 * @param csPin - an enum to specify a port pin
 * this is constructed with the top nibble representing the port and the bottom nibble the pin number
 * @param baudDiv
 * @param clockSpec
 * @param numBytes -  The number of bytes contained in this SPI transaction
 * @param deAssertCs -  If we want to de-assert CS at the end of this SPI transaction
 * @param transactionId -  Unique ID for the transaction
 * @returns - the index of the new message.
 */
BbBlock addSpiTransactionMessage(Bb * buf, SpiDevBbEnum spiDev, PortPinEnum csPin, BaudDivBbEnum baudDiv, ClockSpecBbEnum clockSpec, uint16_t numBytes, bool deAssertCs, int32_t transactionId){
	BbBlock msg = buf->length;
	//Extend buffer to include the main message body before writing it
	buf->length = msg + SPI_TRANSACTION_MESSAGE_LENGTH;
	setBbUint32(buf, msg, SPI_TRANSACTION_MESSAGE_MODULE_MESSAGE_KEY_INDEX, SPI_TRANSACTION_MESSAGE_MODULE_MESSAGE_KEY);
	setBbUint16(buf, msg, SPI_TRANSACTION_MESSAGE_LENGTH_INDEX, SPI_TRANSACTION_MESSAGE_LENGTH/4);//length field is measured in 4-byte words
	setBbUint8(buf, msg, SPI_TRANSACTION_MESSAGE_MAX_ORDINAL_INDEX, SPI_TRANSACTION_MESSAGE_MAX_ORDINAL);
	setBbUint8(buf, msg, SPI_TRANSACTION_MESSAGE_SPI_DEV_INDEX, spiDev);
	setBbUint8(buf, msg, SPI_TRANSACTION_MESSAGE_CS_PIN_INDEX, csPin);
	setBbUint8(buf, msg, SPI_TRANSACTION_MESSAGE_BAUD_DIV_INDEX, baudDiv);
	setBbUint8(buf, msg, SPI_TRANSACTION_MESSAGE_CLOCK_SPEC_INDEX, clockSpec);
	setBbUint16(buf, msg, SPI_TRANSACTION_MESSAGE_NUM_BYTES_INDEX, numBytes);
	setBbBool(buf, msg, SPI_TRANSACTION_MESSAGE_DE_ASSERT_CS_INDEX, SPI_TRANSACTION_MESSAGE_DE_ASSERT_CS_MASK, deAssertCs);
	setBbInt32(buf, msg, SPI_TRANSACTION_MESSAGE_TRANSACTION_ID_INDEX, transactionId);
	setBbUint16(buf, msg, SPI_TRANSACTION_MESSAGE_TRANSACTION_DATA_PLACEHOLDER_INDEX, BB_INVALID_BLOCK);//clear sequence header
	return msg;
}
