/**
 * Adds a Adc Config Message to the end of the current buffer
 * A message to convey config parameters for ADCs
 * @param buf - the message buffer to add the message to
 * @returns - the index of the new message.
 */
BbBlock addAdcConfigMessage(Bb * buf){
	BbBlock msg = buf->length;
	//Extend buffer to include the main message body before writing it
	buf->length = msg + ADC_CONFIG_MESSAGE_LENGTH;
	setBbUint32(buf, msg, ADC_CONFIG_MESSAGE_MODULE_MESSAGE_KEY_INDEX, ADC_CONFIG_MESSAGE_MODULE_MESSAGE_KEY);
	setBbUint16(buf, msg, ADC_CONFIG_MESSAGE_LENGTH_INDEX, ADC_CONFIG_MESSAGE_LENGTH/4);//length field is measured in 4-byte words
	setBbUint8(buf, msg, ADC_CONFIG_MESSAGE_MAX_ORDINAL_INDEX, ADC_CONFIG_MESSAGE_MAX_ORDINAL);
	setBbUint16(buf, msg, ADC_CONFIG_MESSAGE_CONFIG_PLACEHOLDER_INDEX, BB_INVALID_BLOCK);//clear sequence header
	return msg;
}
