/**
 * Adds a Flash Program Message to the end of the current buffer
 * a message to convey flash programming data
 * @param buf - the message buffer to add the message to
 * @param state
 * @param version
 * @param crc - a crc of the words sent so far. This is an ethernet CRC-32
 * @param start - the number of words transferred so far, and the start index of the next array of data. Peripherals will indicate the total words that have passed CRC here.
 * @param end - the number of words that will have been transferred after the following array. This is also the index of the first element of the array after this one. Peripherals will indicate the total number of words received here.
 * @param count - the total number of words that will be transferred. When count equals total length then the transfer is complete
 * @returns - the index of the new message.
 */
BbBlock addFlashProgramMessage(Bb * buf, FlashProgramStateEnum state, uint32_t version, uint32_t crc, uint32_t start, uint32_t end, uint32_t count){
	BbBlock msg = buf->length;
	//Extend buffer to include the main message body before writing it
	buf->length = msg + FLASH_PROGRAM_MESSAGE_LENGTH;
	setBbUint32(buf, msg, FLASH_PROGRAM_MESSAGE_MODULE_MESSAGE_KEY_INDEX, FLASH_PROGRAM_MESSAGE_MODULE_MESSAGE_KEY);
	setBbUint16(buf, msg, FLASH_PROGRAM_MESSAGE_LENGTH_INDEX, FLASH_PROGRAM_MESSAGE_LENGTH/4);//length field is measured in 4-byte words
	setBbUint8(buf, msg, FLASH_PROGRAM_MESSAGE_MAX_ORDINAL_INDEX, FLASH_PROGRAM_MESSAGE_MAX_ORDINAL);
	setBbUint8(buf, msg, FLASH_PROGRAM_MESSAGE_STATE_INDEX, state);
	setBbUint32(buf, msg, FLASH_PROGRAM_MESSAGE_VERSION_INDEX, version);
	setBbUint32(buf, msg, FLASH_PROGRAM_MESSAGE_CRC_INDEX, crc);
	setBbUint32(buf, msg, FLASH_PROGRAM_MESSAGE_START_INDEX, start);
	setBbUint32(buf, msg, FLASH_PROGRAM_MESSAGE_END_INDEX, end);
	setBbUint32(buf, msg, FLASH_PROGRAM_MESSAGE_COUNT_INDEX, count);
	setBbUint16(buf, msg, FLASH_PROGRAM_MESSAGE_DATA_PLACEHOLDER_INDEX, BB_INVALID_BLOCK);//clear sequence header
	return msg;
}
